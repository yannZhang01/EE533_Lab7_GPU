// ============================================================
// Instruction Memory (Full Example Program Set)
// 256 instructions
// ============================================================

module instruction_memory (
    input  [31:0] addr,
    output [31:0] instr
);

    reg [31:0] mem [0:255];

    wire [7:0] index;
    assign index = addr[9:2];  // 4-byte aligned

    assign instr = mem[index];

    // ========================================================
    // Example Full Program
    // ========================================================
    initial begin

        // ----------------------------------------------------
        // 0: NOP
        // ----------------------------------------------------
        mem[0] = 32'b0;

        // ----------------------------------------------------
        // Load data from memory
        // r1 = MEM[0]  (10)
        // r2 = MEM[8]  (20)
        // ----------------------------------------------------
        mem[1] = {4'b1001,2'b00,4'd1,4'd0,18'd0};   // LD r1, [0]
        mem[2] = {4'b1001,2'b00,4'd2,4'd0,18'd8};   // LD r2, [8]

        // ----------------------------------------------------
        // Arithmetic operations
        // r3 = r1 + r2
        // r4 = r3 - r1
        // r5 = r4 * r2
        // ----------------------------------------------------
        mem[3] = {4'b0001,2'b00,4'd3,4'd1,4'd2,14'd0}; // ADD
        mem[4] = {4'b0010,2'b00,4'd4,4'd3,4'd1,14'd0}; // SUB
        mem[5] = {4'b0110,2'b00,4'd5,4'd4,4'd2,14'd0}; // MUL

        // ----------------------------------------------------
        // Logical operations
        // r6 = r1 AND r2
        // r7 = r1 OR  r2
        // r8 = r1 XOR r2
        // ----------------------------------------------------
        mem[6] = {4'b0011,2'b00,4'd6,4'd1,4'd2,14'd0}; // AND
        mem[7] = {4'b0100,2'b00,4'd7,4'd1,4'd2,14'd0}; // OR
        mem[8] = {4'b0101,2'b00,4'd8,4'd1,4'd2,14'd0}; // XOR

        // ----------------------------------------------------
        // ADDI test
        // r9 = r1 + 5
        // ----------------------------------------------------
        mem[9] = {4'b1000,2'b00,4'd9,4'd1,18'd5};

        // ----------------------------------------------------
        // Branch test
        // if r9 != 0 jump ahead 2 instructions
        // ----------------------------------------------------
     // BRNZ r9, +1  (跳到 mem[12])
        mem[10] = {4'b1100, 2'b00, 4'd0, 4'd9, 18'd1}; // opcode=BRNZ, dtype=0, rs1=9, imm=1

        mem[11] = 32'b0; // NOP (should skip)

        // ----------------------------------------------------
        // Tensor Load
        // r10 = MEM[16]  (vector A)
        // r11 = MEM[24]  (vector B)
        // ----------------------------------------------------
        mem[12] = {4'b1001,2'b10,4'd10,4'd0,18'd16}; // LD r10
        mem[13] = {4'b1001,2'b10,4'd11,4'd0,18'd24}; // LD r11

        // ----------------------------------------------------
        // Tensor Dot
        // r12 = dot(r10, r11)
        // ----------------------------------------------------
        mem[14] = {4'b1110,2'b10,4'd12,4'd10,4'd11,14'd0};

        // ----------------------------------------------------
        // ReLU
        // r12 = relu(r12)
        // ----------------------------------------------------
        mem[15] = {4'b0111,2'b00,4'd12,4'd12,4'd0,14'd0};

        // ----------------------------------------------------
      // STORE: MEM[32] = r12  -> rs1 = base(0), rs2 = r12, imm = 32
        mem[16] = {4'b1010, 2'b00, 4'd0, 4'd12, 18'd32}; // opcode=ST, dtype=0, rs1=0, rs2=12, imm=32

        // ----------------------------------------------------
        // Infinite Loop
        // ----------------------------------------------------
        mem[17] = {4'b1101,2'b00,28'd17};

        // ----------------------------------------------------
        // Fill remaining memory with NOP
        // ----------------------------------------------------
        mem[18] = 32'b0;
        mem[19] = 32'b0;
        mem[20] = 32'b0;

    end

endmodule